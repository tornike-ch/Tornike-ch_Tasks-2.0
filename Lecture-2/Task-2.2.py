"""
Დაწერეთ პროგრამა რომელიც მიიღებს ორ რიცხვს,დაადგენსპირველი რიცხვი არის
თუ არა მეორე რიცხვის ჯერადი და დაბეჭდავს ეკრანზე. A რიცხვს ეწოდება B რიცხვის
ჯერადი, თუ A = B * n, სადაც n ნატურალური რიცხვია. 
"""
first_number = int(input("შეიყვანეთ პირველი რიცხვი: "))
secont_number = int(input("შეიყვანეთ მეორე რიცხვი: "))
modulo = first_number % secont_number

if modulo == 0:
    print("პირველი რიცხვი მეორეს ჯერადია")
else:
    print("პირველი რიცხვი არ არის მეორეს ჯერადი")
